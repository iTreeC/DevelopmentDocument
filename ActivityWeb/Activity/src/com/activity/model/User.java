package com.activity.model;

/**
 * 功能：实体类
 * @author 马大洲
 * @time 2015/12/21
 *
 */
public class User {
	private int id;
	private String name;
	private String passwd;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	
	
}
